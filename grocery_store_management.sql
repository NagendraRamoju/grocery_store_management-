-- 27 How does the unit price vary across products and orders?
SELECT
    p.prod_id,
    p.prod_name AS product_name,
    p.price AS listed_price,
    ROUND(MIN(od.each_price), 2) AS min_order_unit_price,
    ROUND(MAX(od.each_price), 2) AS max_order_unit_price,
    ROUND(AVG(od.each_price), 2) AS avg_order_unit_price,
    ROUND(STDDEV(od.each_price), 2) AS stddev_unit_price
FROM products p
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name, p.price
ORDER BY stddev_unit_price DESC;




