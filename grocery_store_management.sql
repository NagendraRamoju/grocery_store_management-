-- 14 Monthly trends in order volume and revenue
SELECT
    MONTH(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS month_num,
    MONTHNAME(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS month_name,
    YEAR(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS order_year,
    COUNT(DISTINCT o.ord_id) AS total_orders,
    ROUND(SUM(od.total_price), 2) AS total_revenue
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY order_year, month_num, month_name
ORDER BY order_year, month_num;

-- 15  How do order patterns vary across weekdays and weekends?
SELECT
    CASE
        WHEN DAYOFWEEK(STR_TO_DATE(o.order_date, '%m/%d/%Y')) IN (1, 7)
        THEN 'Weekend'
        ELSE 'Weekday'
    END AS day_type,
    DAYNAME(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS day_name,
    COUNT(DISTINCT o.ord_id) AS total_orders,
    ROUND(SUM(od.total_price), 2) AS total_revenue
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY day_type, day_name
ORDER BY day_type, total_orders DESC;

