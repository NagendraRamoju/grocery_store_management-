-- 23 What is the average order value handled per employee?
SELECT
    e.emp_id,
    e.emp_name AS employee_name,
    ROUND(AVG(order_totals.order_total), 2) AS avg_order_value
FROM employees e
JOIN orders o ON e.emp_id = o.emp_id
JOIN (
    SELECT ord_id, SUM(total_price) AS order_total
    FROM order_details
    GROUP BY ord_id
) AS order_totals ON o.ord_id = order_totals.ord_id
GROUP BY e.emp_id, e.emp_name
ORDER BY avg_order_value DESC;

-- SECTION 6: ORDER DETAILS DEEP DIVE
-- 25 What is the relationship between quantity ordered and total price?
SELECT
    quantity,
    COUNT(*) AS num_line_items,
    ROUND(AVG(each_price), 2) AS avg_unit_price,
    ROUND(AVG(total_price), 2) AS avg_total_price,
    ROUND(SUM(total_price), 2) AS sum_total_price
FROM order_details
GROUP BY quantity
ORDER BY quantity;


